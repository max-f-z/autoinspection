package com.autoinspection.polaris.vo.wx;

public class SignUpResponse {
	private String token;
	private int id;
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
}
